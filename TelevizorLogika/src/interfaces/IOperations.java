package interfaces;

public interface IOperations {

	/**
	 * Increments channel by 1
	 */
	void PlusChannel();	
	
	/**
	 * Decrements channel by 1
	 */
	void MinusChannel();
	
	/**
	 * Increments volume by 1
	 */
	void PlusVol();
	
	/**
	 * Decrements volume by 1
	 */
	void MinusVol();
	
	
}

